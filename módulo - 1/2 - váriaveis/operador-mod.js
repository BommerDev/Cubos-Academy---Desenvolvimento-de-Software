// Vamos definir as váriaveis
let dividendo = 30, divisor = 20;


// Quociente 
let quociente = dividendo / divisor;

// Resultado do quociente
console.log('Quociente da divisão, é: ' + quociente);

// Operador mod, para resto de divisão
let resto = dividendo % divisor;

// Resultado do resto da divisão
console.log('O resto da divisão, é: ' + resto);