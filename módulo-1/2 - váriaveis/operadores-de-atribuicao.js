// Váriaveis declaradas
let numero1 = 10, numero2 = 3;

// atribuição, +=, soma da esquerda para a direita
numero1 += numero2;

// Resultado
console.log('Atribuição +=, resultado: ' + numero1);

// atribuição, -=, subtração da esquerda para a direita
numero1 -= numero2;

// Resultado
console.log('Atribuição -=, resultado: ' + numero1);

// atribuição, *=, multiplicação da esquerda para a direita
numero1 *= numero2;

// Resultado
console.log('Atribuição *=, resultado: ' + numero1);

// atribuição, /=, divisão da esquerda para a direita
numero1 /= numero2;

// Resultado
console.log('Atribuição /=, resultado: ' + numero1);

// atribuição, %=, resto da divisão da esquerda para a direita
numero1 %= numero2;
console.log('Atribuição %=, resultado: ' + numero1);