let numero1 = 5;
let numero2 = 10;

// Adição = +
let adicao = numero1 + numero2;
console.log('Resultado da adição = ' + adicao);

// Subtração = -
let subtracao = numero2 - numero1;
console.log('Resultado da subtração = ' + subtracao);

// Multiplicação = *
let multiplicacao = numero1 * numero2;
console.log('Resultado da multiplicação = ' + multiplicacao);

// Divisão = /
let divisao = numero2 / numero1;
console.log('Resultado da divisão = ' + divisao);

// Explicação
console.log('\n operações feitas com os numeros, "10" e "5"');

