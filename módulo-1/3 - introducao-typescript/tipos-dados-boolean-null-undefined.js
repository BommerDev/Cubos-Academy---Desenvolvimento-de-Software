// Dado boolean - true - false
let ativo = true;
let desativo = false;
console.log(typeof ativo);
console.log(typeof desativo);

// Dado Null - object
let cor = 'Azul';
console.log(typeof cor);
cor = null;
console.log(typeof cor);

// Dado Undefined - Sem Valor
let carro;
console.log(typeof carro);
