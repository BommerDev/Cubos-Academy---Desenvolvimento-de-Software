/* Vamos imaginar o seguinte problema: Estamos somando um valor String ao Numérico */

// Variável do tipo numérico
let numero1 = 20;

// Variável do tipo String
let numero2 = '12';

// Concatenação entre as variáveis, já que uma string, não se soma uma numero
let resultado = numero1 + numero2;

// Resultado da soma
console.log(resultado);

/* Podemos perceber que no JS, os dados são dinâmicos */ 